#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t size)
{
	t_list	*list;

	list = (t_list *)malloc(sizeof(*list));
	if (!list)
		return (NULL);
	if (!content)
	{
		list->content = NULL;
		list->content_size = 0;
	}
	else
	{
		list->content = malloc(size);
		if (!list->content)
			return (NULL);
		ft_memcpy(list->content, content, size);
		list->content_size = size;
	}
	list->next = NULL;
	return (list);
}
